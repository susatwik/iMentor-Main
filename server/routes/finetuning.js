// 🎓 ACADEMIC VALIDATION for Quiz/Question Generation
// Mirrors the server-side patterns in chat.js, applied at the data layer.
// ─────────────────────────────────────────────────────────────────────────────
const NON_ACADEMIC_COURSE_PATTERNS = [
    /\b(movies|films|cinema|netflix|series|tv show|web series|anime|drama|episode|season|actor|actress|director|bollywood|hollywood|ott|streaming)\b/i,
    /\b(cricket|football|soccer|basketball|tennis|ipl|fifa|nba|match|score|player|team|tournament|league|gaming|video game|fortnite|pubg|minecraft|gta|valorant|esports)\b/i,
    /\b(instagram|facebook|twitter|tiktok|youtube|snapchat|celebrity|influencer|viral|meme|trending|followers|likes|reels|shorts)\b/i,
    /\b(recipe|cooking|cake|pizza|biryani|restaurant|food|diet|workout|gym|fashion|clothing|style|hair|makeup|skincare)\b/i,
    /\b(gossip|rumor|scandal|affair|breakup|relationship advice|dating|marriage tips|love advice)\b/i,
    /\b(joke|funny|meme|roast|riddle|prank)\b/i,
];

function isCourseAcademic(courseName) {
    if (!courseName) return true; // no course = don't block
    const name = courseName.toLowerCase();

    // Exception for Film Studies / Cinema which are academic
    if (name.includes('syllabus') || name.includes('studies') || name.includes('history') || name.includes('theory')) {
        return true;
    }

    return !NON_ACADEMIC_COURSE_PATTERNS.some(p => p.test(courseName));
}

// @route   GET /api/admin/finetuning/questions
// @desc    Returns QA pairs for a selected course for practice/quiz use.
//          Validates that the selected course is academic before returning data.
// @access  Authenticated users (no admin auth needed — used by AI Assistant mode)
router.get('/questions', authMiddleware, async (req, res) => {
    const { course } = req.query;

    // ── Academic validation ───────────────────────────────────────────────────
    if (course && !isCourseAcademic(course)) {
        console.log(`[Finetuning] 🚫 Non-academic course rejected for quiz: "${course}"`);
        return res.status(422).json({
            success: false,
            isNonAcademic: true,
            course,
            message: `"${course}" is not an academic subject. The AI Practice Quiz only supports academic courses (e.g., Mathematics, Physics, Computer Science, Engineering).`
        });
    }

    try {
        const ragDataDir = path.join(__dirname, '..', 'rag_service', 'data', 'training_sets');

        let files;
        try {
            files = await fs.readdir(ragDataDir);
        } catch {
            return res.json({ success: true, questions: [], message: 'No QA datasets found yet.' });
        }

        const jsonFiles = files.filter(f => f.endsWith('.json'));
        if (jsonFiles.length === 0) {
            return res.json({ success: true, questions: [], message: 'No QA datasets found yet.' });
        }

        // Get metadata (modification time) for all files
        const fileStats = await Promise.all(jsonFiles.map(async f => {
            const stats = await fs.stat(path.join(ragDataDir, f));
            return { name: f, time: stats.mtime.getTime() };
        }));
        fileStats.sort((a, b) => b.time - a.time); // newest first

        // Try to find a course-specific dataset first
        let targetFile = null;
        if (course) {
            const courseSlug = course.toLowerCase().replace(/\s+/g, '_').replace(/[^a-z0-9_]/g, '');
            const courseSpecific = fileStats.find(f =>
                f.name.toLowerCase().includes(courseSlug) ||
                f.name.toLowerCase().includes(course.toLowerCase().replace(/\s+/g, '-'))
            );
            if (courseSpecific) {
                targetFile = courseSpecific;
                console.log(`[Finetuning] Found course-specific dataset for "${course}": ${courseSpecific.name}`);
            }
        }

        // Fall back to latest dataset
        if (!targetFile) {
            targetFile = fileStats[0];
            console.log(`[Finetuning] No course-specific dataset found for "${course || 'unspecified'}". Using latest: ${targetFile.name}`);
        }

        const latestFile = path.join(ragDataDir, targetFile.name);
        const content = await fs.readFile(latestFile, 'utf8');
        const questions = JSON.parse(content);

        res.json({
            success: true,
            questions,
            total: questions.length,
            source: targetFile.name,
            course: course || null
        });
    } catch (err) {
        console.error('[Finetuning] Error reading questions:', err.message);
        res.status(500).json({ success: false, message: 'Failed to load questions.' });
    }
});



